<h2>Archelon について</h2>

<p>Archelon は アカウントアグリゲーションサービスの一種です。RESTful APIなどなどウェブAPIを用意していないウェブアプリケーションにAPIを追加するシステムです。</p>
<p>フロントエンドとバックエンドを担う Archelon と、各種ウェブアプリケーションにログインし情報を集めウェブAPIとしてのインターフェースを用意するコネクタとで構成されています。</p>

